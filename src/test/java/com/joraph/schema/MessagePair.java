package com.joraph.schema;

public class MessagePair {

	private String left;
	private String right;

	public String getLeft() {
		return left;
	}

	public MessagePair setLeft(String left) {
		this.left = left;
		return this;
	}

	public String getRight() {
		return right;
	}

	public MessagePair setRight(String right) {
		this.right = right;
		return this;
	}

}
